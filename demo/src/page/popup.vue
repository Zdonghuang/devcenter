<script>
export default {
  name: "appStore",
  data() {
    return {
      appData: [],
      indx: "-1"
    };
  },
  created() {},
  mounted() {
    this.showData();
  },
  methods: {
    getImageUrl(url) {
      return this.$imageUrl + url;
    },
    getmaxversion(a, b) {
      let numa = a.version.split(".");
      let numb = b.version.split(".");
      if (numa.length > numb.length) {
        for (var i = 0; i < numa.length; i++) {
          numa[i] = parseInt(numa[i]);
          if (numb[i]) {
            numb[i] = parseInt(numb[i]);
          }
          if (numa[i] > numb[i]) {
            return a;
          } else if (numa[i] === numb[i]) {
            continue;
          } else {
            return b;
          }
        }
      } else {
        for (var i = 0; i < numb.length; i++) {
          numb[i] = parseInt(numb[i]);
          if (numa[i]) {
            numa[i] = parseInt(numa[i]);
          }
          if (numa[i] > numb[i]) {
            return a;
          } else if (numa[i] === numb[i]) {
            continue;
          } else {
            return b;
          }
        }
      }
    },

    showData() {
      let _this = this;
      _this.$ajax.get("/v2/apps/applist").then(res => {
        var map = {};

        for (var i = 0; i < res.data.result.length; i++) {
          var key = res.data.result[i].bundleId;
          if (map[key]) {
            map[key] = this.getmaxversion(map[key], res.data.result[i]);
          } else {
            map[key] = res.data.result[i];
          }
        }
        this.appData = map;
        // console.log(this.appData);
      });
    },
    buyapp(id, indxx) {
      if (!this.$store.state.usertoken) {
        this.$alert("亲～请登陆才能购买哦。。");
        this.$router.push("/login");
      } else {
        let _this = this;
        let json = JSON.stringify({ username: "", appname: "", appid: id });
        _this.$ajax
          .post("/v2/users/addApp", json)
          .then(res => {
            // res.setRequestHeader("Authorization", this.$store.state.usertoken);
            if (res.status == "ok") {
              this.$alert("购买成功");
              this.indx = indxx;
            }
          })
          .catch(err => {
            if (err) {
              this.$alert("亲～请登陆才能购买哦。。");
            }
          });
      }
    }
  }
};
